        with st.spinner("Sedang memindai artefak spasial dan konsistensi temporal video..."):
            ai_score, total_frames = analyze_video_frames(temp_file_path)

        # Menentukan label status keaslian
        if ai_score > 50.0:
            st.error(f"⚠️ **TERDETEKSI MANIPULASI AI / DEEPFAKE**")
            real_score = 100.0 - ai_score
        else:
            st.success(f"✅ **VIDEO TERINDIKASI ASLI (KOREK)**")
            real_score = 100.0 - ai_score

        # Menampilkan metrics indikator persentase
        m1, m2 = st.columns(2)
        m1.metric(label="Probabilitas Hasil Generative AI", value=f"{ai_score} %")
        m2.metric(label="Tingkat Keaslian (Original Kameraphone)", value=f"{round(real_score, 2)} %")

        # Informasi Metadata Tambahan
        st.info(f"**Metadata Ekstraksi:**\n* Total Frame Terbaca: {total_frames} frame\n* Ekstraksi Fitur Spasial: Laplacian Variance Mapping\n* Metode Analisis Temporal: Frame-by-Frame Noise Tracking")

